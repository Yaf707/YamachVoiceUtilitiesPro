export type I18nKey = keyof typeof strings.ar;

export function isArabic() {
    return settings.store.language === UiLanguage.Arabic;
}

export function dir() {
    return isArabic() ? "rtl" : "ltr";
}

export function t(key: I18nKey) {
    return strings[settings.store.language as UiLanguage]?.[key] ?? strings.en[key];
}
